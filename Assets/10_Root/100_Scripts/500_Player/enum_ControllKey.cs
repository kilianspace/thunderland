public enum ControllerKey
{
    // Face Buttons
    Cross,      // The "X" button
    Circle,     // The "O" button
    Square,     // The "Square" button
    Triangle,   // The "Triangle" button

    // D-Pad
    DPadUp,
    DPadDown,
    DPadLeft,
    DPadRight,

    // Shoulder Buttons
    L1,         // Left shoulder button 1
    R1,         // Right shoulder button 1

    // Triggers
    L2,         // Left trigger button (analog)
    R2,         // Right trigger button (analog)

    // Joysticks
    LeftStickX, // Left joystick horizontal movement
    LeftStickY, // Left joystick vertical movement
    RightStickX,// Right joystick horizontal movement
    RightStickY,// Right joystick vertical movement

    // Joystick Press
    LeftStickPress,  // Pressing the left joystick
    RightStickPress, // Pressing the right joystick

    // Touchpad
    TouchpadClick,   // Pressing the touchpad

    // Options and Share Buttons
    Options,    // Options button (start)
    Share,      // Share button (select)

    // PlayStation Button
    PSButton,   // PlayStation button

    // Touchpad and Motion Sensor (if needed)
    TouchpadSwipeUp,
    TouchpadSwipeDown,
    TouchpadSwipeLeft,
    TouchpadSwipeRight,

    // Gyroscope/Motion Sensor
    Gyro,
    Accelerometer
}
